function add(a, b) {
return a + b
}
function subtract(a, b) {
    return a - b
}
function multiply(a, b) {
    return a * b
}
function divide(a,b) {
    return a / b
}

let n = 10 
function increment(n) {
    return (++n);
}
function decrement(n) {
    return (--n);
}

function makeInt(n) {
    parseInt(n)
    return parseFloat(n)
}

function preserveDecimal(n) {
    parseFloat(n, 10)
    return parseFloat(n)
}